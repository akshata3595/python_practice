#Example for String Operators.

#Concatenation (+)
var1 = 'Python'
var2 = 'String'
print (var1+var2)

#Repetition (*)
var1 = 'Python'
print (var1*3)

#Slicing []
var1 = 'Python'
print (var1[2])

#Range Slicing [x:y]
var1 = 'Python'
print (var1[2:5])

#Membership (in)
var1 = 'Python'
print ('n' in var1)

#Membership (not in)
var1 = 'Python'
print ('N' not in var1)


